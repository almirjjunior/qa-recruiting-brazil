
public class Rules {
	
	private int A;
	private int B;
	private int C;
	private int D;
	
	
	Rules(int A, int B,int C,int D ){
		setA(A);
		setB(B);
		setC(C);
		setD(D);
		
	}
	
	public int getA() {
		return A;
	}
	public void setA(int a) {
		A = a;
	}
	public int getB() {
		return B;
	}
	public void setB(int b) {
		B = b;
	}
	public int getC() {
		return C;
	}
	public void setC(int c) {
		C = c;
	}
	public int getD() {
		return D;
	}
	public void setD(int d) {
		D = d;	
	}
	
}
